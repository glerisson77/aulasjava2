package aula58;

public class TesteAutoboxing {

    public static void main(String[] args) {

        //autoboxing
        Short num7 = 1;
        Byte num8 = 10;
        Integer num9 = 100;
        int n = 1999999999;
        Long num10 = 10000000L;
        Float num11 = 3.5f;
        Double num12 = 3.555;

        //auto unboxing
        int num13 = num9;
    }
}
